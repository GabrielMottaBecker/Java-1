
//Crie um programa que declare vari√°veis de diferentes tipos (int, double, boolean, char) e as inicialize com valores apropriados. Em seguida, imprima esses valores no console.

public class Main {
    public static void main(String[] args) {
        int x = 100;
        System.out.println("x: " + x);

        char caractere = 'a';
        System.out.println("caractere: " + caractere);

        String str = "Hello World";
        System.out.println("str: " + str);

        double num = 1.0;
        System.out.println("num: " + num);

        float real = 1.0f;
        System.out.println("real: " + real);

        long longVar = 1L;
        System.out.println("LongVar: " + longVar);

        byte byteVar = 100;
        System.out.println("byteVar: " + byteVar);

        boolean isJavaFun = true;
        boolean isFishTasty = false;
        System.out.println("isJavaFun: " + isJavaFun);
        System.out.println("isFishTasty: " + isFishTasty);
    }
}